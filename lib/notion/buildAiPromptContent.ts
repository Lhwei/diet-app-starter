// lib/notion/buildAiPromptContent.ts
// 純文字組裝：把三個資料庫ID與分析Prompt範本組成要寫入「AI用PROMPT」頁面的內容
// 內容使用markdown語法（# 標題、``` 程式碼區塊等），
// 實際寫入Notion前，請搭配 markdownToNotionBlocks() 轉換成正確的block結構，
// 才能在Notion裡呈現真正的標題階層與灰底Prompt區塊樣式。
//
// 注意：這裡用了 ``` 包住Prompt範本，屬於markdown語法本身的一部分，
// 不是要在template literal裡直接使用反引號變數插值，因此不會有parse error風險
// ——但仍要小心，${} 這種插值只能用在最外層的 personalDbId 等變數上，
// 內容本文（例如Prompt範本文字）不可以再包含反引號字元。

export function buildAiPromptContent(params: {
  personalDbId: string
  physioDbId: string
  dietDbId: string
}) {
  const { personalDbId, physioDbId, dietDbId } = params

  return `# AI 使用說明：飲食管理小幫手資料索引
 
本頁面提供給具備 Notion 存取能力的 AI 助手使用。第一、二節是「背景知識」，
AI 每次分析前都要讀取；第三節是「可直接複製貼上」的 Prompt，每個都是
獨立、完整、可單獨使用的指令，不需要再回頭翻閱其他章節。
 
---
 
## 一、資料位置索引
 
**【個人資料】** database_id: ${personalDbId}
健康目標（減脂／增肌／控糖／維持）、目標體重、BMI、BMR、TDEE、
慢性病史、年齡、性別、活動量。
 
**【生理紀錄】** database_id: ${physioDbId}
體重、血糖、血壓，含量測日期。
 
**【飲食紀錄】** database_id: ${dietDbId}
日期、餐別、食物內容、六大類食物份數、三大營養素、總熱量、
飽足感、油脂感知、精神狀態、身體不適標記、場景來源、備註。
 
---
 
## 二、使用原則（所有 Prompt 共用，AI 必須遵守）
 
1. 以上 database 僅供讀取，禁止新增、更新、刪除任何資料。
2. 預設查詢最近 7 天；資料量不足時延伸至 14 天；使用者指定天數則優先採用。延伸後仍不足，需明講「樣本不足，結論僅供參考」。
3. **不得只列數字**，必須說明「健康目標 → 生理趨勢 → 飲食原因 → 建議」彼此的因果關係；資料無法支持推論時，直接說「目前無法判斷」，不得臆測。
4. 每項建議必須具體可執行，禁止「多運動」「飲食均衡」等籠統說法，且需包含**建議內容 / 原因 / 預期改善方向**三部分。
5. 不使用醫療診斷用語；數值明顯異常時提醒諮詢醫療人員，不做病名推測。
6. 語氣溫和具體，避免說教或加重焦慮。
7. 除非該 Prompt 另有說明，輸出一律採用四區塊：**①生理趨勢 ②可能原因 ③做得好的地方 ④三個具體建議**。
 
---
 
## 三、Prompt 範本（可直接複製貼上使用）
 
### Prompt A｜整體健康分析（預設用這個）
 
\`\`\`
依序查詢個人資料(${personalDbId}) → 生理紀錄(${physioDbId}，近7天，不足延伸14天)
→ 飲食紀錄(${dietDbId}，同區間)。
 
先依健康目標列出這次分析重點(例如減脂則看：體重是否下降/熱量是否赤字/蛋白質是否足夠)。
接著比對生理趨勢與飲食內容，建立「目標→生理趨勢→飲食原因」的因果關係，
不得只列數字、資料不足時要說明無法判斷。
 
輸出四區塊：
①生理趨勢 ②可能原因 ③做得好的地方 ④三個具體建議
(每項建議需包含：建議內容／原因／預期改善方向)
\`\`\`
 
### Prompt B｜飲食均衡度分析
 
\`\`\`
查詢飲食紀錄(${dietDbId}，近7天，不足延伸14天)。
計算平均每日六大類食物份數並對照建議份數，計算三大營養素比例是否落在建議範圍，
標註連續缺乏特定食物類別的情況，並說明「缺乏該類別→可能造成的影響」
(例如對飽足感、精神狀態的影響)，不得只列數字。
 
輸出四區塊，給出2-3項最優先調整建議
(每項含：建議內容／原因／預期改善方向)。
\`\`\`
 
### Prompt C｜紀錄落差分析（理論 vs 實際）
 
\`\`\`
查詢個人資料(${personalDbId})確認目標與TDEE → 生理紀錄(${physioDbId}，近2-4週體重趨勢)
→ 同期飲食紀錄(${dietDbId})。
 
依平均攝取熱量估算理論體重變化，與實際體重變化比對，
說明落差可能原因(記錄習慣、漏記傾向、頻率等)；
若無法判斷落差是「漏記」還是「實際攝取」，需明講目前無法判斷，不得臆測。
 
輸出四區塊，建議語氣溫和(避免讓使用者覺得被指責沒有誠實記錄)。
\`\`\`
 
### Prompt D｜精神／身體狀態與飲食關聯分析
 
\`\`\`
查詢飲食紀錄(${dietDbId}，近14天)。
比較「精神狀態」「身體不適標記」在不同飲食內容、份量、熱量、用餐時間下的分布差異，
建立「特定飲食型態→精神/身體狀態變化」的關聯，不得只列數字。
 
若身體不適標記疑似與疾病症狀相關，於建議區塊外加註提醒諮詢醫療人員。
輸出四區塊，給出2-3項可觀察或調整的方向。
\`\`\`
 
### Prompt E｜用餐情境分析（外食／自煮／加班／聚餐）
 
\`\`\`
查詢飲食紀錄(${dietDbId}，近14-30天)，依「場景來源」分組。
比較不同情境下六大類份數、三大營養素比例、總熱量、飽足感／油脂感知的差異，
找出最容易偏離目標的情境並說明原因。
 
輸出四區塊，建議需針對該情境設計(例如加班日的預先備餐、外食點餐原則)，
不得給籠統建議。
\`\`\`
 
### Prompt F｜週間 vs 週末比較分析
 
\`\`\`
查詢近4週飲食紀錄(${dietDbId})與生理紀錄(${physioDbId})。
分別彙整週一至週五、週六日的平均熱量、六大類份數、體重/血糖變化，
比較兩者差異，判斷週末是否為主要落差來源並說明原因(場景來源、聚餐頻率等)。
 
輸出四區塊，建議鎖定週末情境。
\`\`\`
 
### Prompt G｜生理數值與飲食時序關聯分析
 
\`\`\`
查詢近2-4週生理紀錄(${physioDbId}，血糖/血壓)與同期飲食紀錄(${dietDbId})。
依時間序列比對數值變化前1-2天的飲食內容(份量、精緻澱粉/油脂攝取、用餐時間)，
描述觀察到的關聯型態，不做因果診斷。
 
若持續異常，於建議區塊外加註提醒諮詢醫療人員，
並建議記錄更細節的量測情境(時間、是否空腹)。
輸出四區塊。
\`\`\`
 
### Prompt H｜快速健康週報（Quick Check-in）
 
\`\`\`
依序查詢個人資料(${personalDbId}) → 近7天生理紀錄(${physioDbId}) → 近7天飲食紀錄(${dietDbId})。
輸出精簡版：摘要(1句) + 關鍵發現(1-2點) + 重點建議(1-2項，含建議/原因/預期改善)，
總長控制在200字內，適合作為每週固定回顧。
\`\`\`
`
}
